# Task 1: Loop Condition Exploration
counter = 0
while True:  # Infinite loop
    print("Inside the loop")
    counter += 1
    if counter >= 5:
        break  # Exit the loop after 5 iterations

# Task 2: Conditional Exit
counter = 0
while counter < 10:  # Condition that will eventually be true
    print("Inside the loop")
    counter += 1
